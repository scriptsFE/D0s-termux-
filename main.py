stats = {
    "success": 0,
    "errors": 0,
    "total": 0
}

async def worker(bot_id, session, target_url, semaphore):
    while True:
        async with semaphore:
            try:
                headers = {
                    "User-Agent": random.choice(USER_AGENTS),
                    "Connection": "keep-alive"
                }
                async with session.get(target_url, headers=headers, timeout=5) as response:
                    if response.status == 200:
                        stats["success"] += 1
                    else:
                        stats["errors"] += 1
                    print(f"[Bot {bot_id}] Visited target (Status {response.status})")
                    sys.stdout.flush()
            except Exception:
                stats["errors"] += 1
            finally:
                stats["total"] += 1

async def stats_reporter():
    start_time = time.time()
    try:
        while True:
            await asyncio.sleep(1.0)
            elapsed = time.time() - start_time
            req_per_sec = stats["total"] / elapsed if elapsed > 0 else 0
            
            sys.stdout.write(
                f"\r[Stats] Success: {stats['success']} | Errors: {stats['errors']} | "
                f"Total {stats['total']} | Speed {req_per_sec:.2f} req/s"
            )
            sys.stdout.flush()
    except asyncio.CancelledError:
        pass

async def main():
    print("=" * 45)
    print("      ASYNC     ")
    print("=" * 45)
    
    target_url = input("Target URL-> ").strip()
    if not target_url:
        print("[!] Target url cannot be empity duh")
        sys.exit(1)
        
    if not target_url.startswith("http://") and not target_url.startswith("https://"):
        target_url = "https://" + target_url
        
    try:
        concurrency_limit = int(input("Max ammount send").strip())
    except ValueError:
        print("[!] Concurrency limit must be a integer")
        sys.exit(1)

    print(f"\n[!] Initializing async engine with a concurrency limit of {concurrency_limit}...")
    
    semaphore = asyncio.Semaphore(concurrency_limit)
    connector = aiohttp.TCPConnector(limit=concurrency_limit, ttl_dns_cache=300)
    
    async with aiohttp.ClientSession(connector=connector) as session:
        reporter_task = asyncio.create_task(stats_reporter())
        workers = [
            asyncio.create_task(worker(i + 1, session, target_url, semaphore))
            for i in range(concurrency_limit)
        ]
        
        try:
            await asyncio.gather(*workers)
        except KeyboardInterrupt:
            print("\n\n[!] Cleaning up tasks..hold up")
        finally:
            reporter_task.cancel()
            for w in workers:
                w.cancel()

if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        print("\n[!] Exited done")
